<?php

namespace framework\models\Entity;

class User
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $email;

    /**
     * @var \Carbon\Carbon
     */
    private $birthDate;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     *
     * @return User
     */
    public function setId(int $id): User
    {
        $this->id = $id;

        return $this;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     *
     * @return User
     */
    public function setName(string $name): User
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getEmail(): string
    {
        return $this->email;
    }

    /**
     * @param string $email
     *
     * @return User
     */
    public function setEmail(string $email): User
    {
        $this->email = $email;

        return $this;
    }

    /**
     * @return \Carbon\Carbon
     */
    public function getBirthDate(): \Carbon\Carbon
    {
        return $this->birthDate;
    }

    /**
     * @param \Carbon\Carbon $birthDate
     *
     * @return User
     */
    public function setBirthDate(\Carbon\Carbon $birthDate): User
    {
        $this->birthDate = $birthDate;

        return $this;
    }
}